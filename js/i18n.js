window.MAAL_applyLang = function (lang) {
  var t = window.MAAL_I18N[lang];
  if (!t) return;

  document.documentElement.lang = lang;
  var metaDesc = document.querySelector('meta[name="description"]');
  if (document.body.classList.contains('page-privacy') && t.privacy) {
    document.title = t.privacy.metaTitle;
    if (metaDesc) metaDesc.content = t.privacy.metaDesc;
    window.MAAL_updateMetaTags(t.privacy.metaTitle, t.privacy.metaDesc, lang);
  } else {
    document.title = t.meta.title;
    if (metaDesc) metaDesc.content = t.meta.description;
    window.MAAL_updateMetaTags(t.meta.ogTitle || t.meta.title, t.meta.ogDescription || t.meta.description, lang);
  }

  document.querySelectorAll('[data-i18n]').forEach(function (el) {
    var key = el.getAttribute('data-i18n');
    var val = key.split('.').reduce(function (o, k) { return o && o[k]; }, t);
    if (val == null) return;
    if (el.getAttribute('data-i18n-html') === 'true') el.innerHTML = val;
    else el.textContent = val;
  });

  /* Marquee rebuild */
  var marquee = document.getElementById('marqueeContent');
  if (marquee && t.marquee) {
    var html = '';
    var items = t.marquee.items.concat(t.marquee.items);
    items.forEach(function (item, i) {
      html += '<span>' + item + '</span>';
      if (i < items.length - 1) html += '<span>•</span>';
    });
    marquee.innerHTML = html;
  }

  /* Stat suffix */
  var stat1 = document.querySelector('[data-count="65.5"]');
  if (stat1) stat1.dataset.suffix = t.stats.suffix1;

  /* Lang toggle label */
  var langBtn = document.getElementById('langToggle');
  if (langBtn) langBtn.textContent = lang === 'id' ? 'EN' : 'ID';

  /* Tech tags rebuild */
  var techTags = document.getElementById('techTags');
  if (techTags && t.services && t.services.tags) {
    techTags.innerHTML = t.services.tags.map(function (tag, i) {
      var cls = i === 4 ? 'tech-tag tech-tag--highlight' : 'tech-tag';
      return '<span class="' + cls + '">' + tag + '</span>';
    }).join('');
  }

  /* Aria labels */
  var backTop = document.getElementById('backToTop');
  if (backTop) backTop.setAttribute('aria-label', t.ui.backTop);
  var waFloat = document.getElementById('whatsappFloat');
  if (waFloat) waFloat.setAttribute('aria-label', t.ui.whatsapp);
  var navToggle = document.getElementById('navToggle');
  if (navToggle) navToggle.setAttribute('aria-label', t.nav.menu);

  localStorage.setItem('maal-lang', lang);
  if (window.MAAL_initWhatsApp) window.MAAL_initWhatsApp();
  if (window.MAAL_initStoreBadges) window.MAAL_initStoreBadges();
};

window.MAAL_updateMetaTags = function (title, description, lang) {
  var setMeta = function (selector, content) {
    var el = document.querySelector(selector);
    if (el && content) el.setAttribute('content', content);
  };
  setMeta('meta[property="og:title"]', title);
  setMeta('meta[property="og:description"]', description);
  setMeta('meta[name="twitter:title"]', title);
  setMeta('meta[name="twitter:description"]', description);
  setMeta('meta[property="og:locale"]', lang === 'en' ? 'en_US' : 'id_ID');
};

window.MAAL_toggleTheme = function () {
  var html = document.documentElement;
  var next = html.getAttribute('data-theme') === 'dark' ? 'light' : 'dark';
  if (next === 'light') html.removeAttribute('data-theme');
  else html.setAttribute('data-theme', 'dark');
  localStorage.setItem('maal-theme', next);
  window.MAAL_updateThemeIcon();
};

